#Pide al usuario dos números y muestra la suma, resta, multiplicación y división (separadas por coma).
num1 = float(input("Por favor ingresa un numero: "))
num2 = float(input("Por favor ingresa un numero: "))
suma = num1 + num2
resta = num1 - num2
multiplicacion = num1 * num2
division = num1 / num2
print(f"El resultado de la suma, resta, multiplicación, y división son: {suma}, {resta}, {multiplicacion}, {division}, respectivamente")